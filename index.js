// -------------------------- DECREASE-THE-QUANTITY -------------------------- //

function minus(ID, orgPrice){
  let Qid = "quantityCounter"+ID;
  let Pid = "cartPrice"+ID;
  const quantityCounter = document.getElementById(Qid).value;
  const value = parseInt(quantityCounter);
  if (value > 1){
    document.getElementById(Qid).value = value - 1;

    const cartPrice = document.getElementById(Pid).innerHTML;
    cartPrice.replace("$","");
    intPrice = parseFloat(cartPrice);
    newPrice = intPrice - parseFloat(orgPrice);
    document.getElementById(Pid).innerHTML = newPrice.toFixed(2)+"$";
  }
  prices.map((value) => {
    if(ID == value.id){
      if(value.qty>1){
        value.qty = value.qty-1;
        value.price = value.price - parseFloat(orgPrice)
      }
    }
  })
  checkoutPrice(prices)
}


// -------------------------- INCREASE-THE-QUANTITY -------------------------- //

function plus(ID, orgPrice){
  let Qid = "quantityCounter"+ID;
  let Pid = "cartPrice"+ID;
  const quantityCounter = document.getElementById(Qid).value;
  const value = parseInt(quantityCounter);
  document.getElementById(Qid).value = value + 1;

  const cartPrice = document.getElementById(Pid).innerHTML;
  cartPrice.replace("$","");
  intPrice = parseFloat(cartPrice);
  newPrice = intPrice + parseFloat(orgPrice);
  document.getElementById(Pid).innerHTML = newPrice+"$";

  prices.map((value) => {
    if(ID == value.id){
      value.qty = value.qty+1;
      value.price = value.price + parseFloat(orgPrice)
    }
  })
  checkoutPrice(prices)
}



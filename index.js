// --------------------------- REMOVE-CART-ITEM --------------------------- //

function removeItem(element, ID) {
  element.closest('.cartData').remove();
  counter -= 1;
  if (parseInt(document.getElementById("cartItemCount").textContent) > 0){
    document.getElementById("cartItemCount").innerHTML = counter;
  }
  data.map((value) => {
    if (value.id == ID){
      document.getElementById(`addTocart${value.id}`).disabled = false;
      document.getElementById(`addTocart${value.id}`).innerHTML = "Add to Cart";
      document.getElementById(`addTocart${value.id}`).style.opacity = 1;
      document.getElementById(`addTocart${value.id}`).style.pointerEvents = 'auto';
      cart.pop(ID);
    }
  })
  checkEmptyCart();
  prices.map((value) => {
    if(ID == value.id){
      value.qty = 0;
      value.price = 0
    }
  })
  checkoutPrice(prices)
}



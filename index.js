// ----------------------------- CUSTOM-DROPDOWN ----------------------------- //

document.addEventListener('DOMContentLoaded', function () {
  var selectedOption = document.getElementById('sort');
  var optionsList = document.getElementById('sortOptions');

  selectedOption.addEventListener('click', function () {
      optionsList.classList.toggle('active');
  });

  optionsList.addEventListener('click', function (event) {
      if (event.target.tagName === 'LI') {
          selectedOption.textContent = event.target.textContent;
          optionsList.classList.remove('active');
      }
  });
});

document.addEventListener('DOMContentLoaded', function () {
  var selectedOption = document.getElementById('categories');
  var optionsList = document.getElementById('categoriesOptions');

  selectedOption.addEventListener('click', function () {
      optionsList.classList.toggle('active');
  });

});


// ------------ MATCHING-SEARCH-DROPDOWN-WIDTH-WITH-SEARCH-INPUT ------------ //

function matchWidth() {
  var topDiv = document.getElementById('search');
  var bottomDiv = document.getElementById('search_options');
  
  bottomDiv.style.width = topDiv.offsetWidth + 'px';
  if (window.innerWidth <= 575) {
      topDiv.style.width = "100%";
    }
  if (window.innerWidth <= 575 & window.innerWidth >= 376){
      bottomDiv.style.width = "85%";
  }
  else if (window.innerWidth <= 376){
      bottomDiv.style.width = "240px";
  }
  }

matchWidth();
window.addEventListener('resize', matchWidth);